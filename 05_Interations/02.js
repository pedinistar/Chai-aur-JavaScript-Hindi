// while and do while
let i = 0
while (i <= 10) {
  console.log(i);
  i = i + 2;
}


const myArr = ["jin","jimin","jk","v"]

let y = 0
while (y <= myArr.length) {
  console.log(`Value is ${myArr[y]}`);
  y = y + 1;
}



let score = 11
do {
  console.log(`score is ${score}`);
  score++;
} while (score<=10);

// score is 11